#Arithmetic Operators

num1 = 34
num2 = 10

print(num1 + num2)
print(num1 - num2)
print(num1 * num2)
print(num1 % num2)
print(num1 / num2)

#comparison Operators
print(num1 ** num2)
print(num1 > num2)
print(num1 < num2)
print(num1 <= num2)
print(num1 == num2)
print(num1 != num2)

w = 0
w +=3
print(w)


#logical operators
print(56<100 and 45>23)
print(56<100 or 45>23)
print(not(56<100 and 45>23))

